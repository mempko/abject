  private async createTypedWidget(
    windowId: AbjectId,
    widget: WidgetAbject,
    rect: Rect
  ): Promise<AbjectId> {
    require(this.uiServerId !== undefined, 'UIServer not set');

    await widget.init(this.bus, this.id);
    this.spawnedWidgets.add(widget.id);
    this.widgetToWindow.set(widget.id, windowId);
    this.log('debug', 'createWidget', { widgetId: widget.id, windowId, type: widget.manifest.name });

    // Only add to window if rect is non-zero.
    // Layout-managed widgets pass {0,0,0,0} and are positioned by their layout.
    if (rect.width > 0 || rect.height > 0) {
      await this.request(
        request(this.id, windowId, 'addChild', {
          widgetId: widget.id,
          rect,
        })
      );
    }

    return widget.id;
  }

  // ── Factory: destroyWindow ────────────────────────────────────────────

  private async destroyWindow(owner: AbjectId, windowId: string): Promise<boolean> {
    const winAbjectId = this.shimWindowMap.get(windowId);
    if (!winAbjectId) return false;

    // Verify ownership
    if (this.windowOwners.get(winAbjectId) !== owner) return false;

    // Unregister from WindowManager
    if (this.windowManagerId) {
      try {
        this.send(event(this.id, this.windowManagerId,
          'unregisterWindow', { windowId: winAbjectId }));
      } catch { /* WM may be gone */ }
    }

    // Send destroy message to WindowAbject (it will destroy all children)
    try {
      await this.request(
        request(this.id, winAbjectId, 'destroy', {})
      );
    } catch {
      // Window may already be stopped
    }

    // Clean up shim maps
    this.shimWindowMap.delete(windowId);
    this.windowOwners.delete(winAbjectId);
    this.spawnedWindows.delete(winAbjectId);

    // Clean up widget shim entries for this window
    for (const [shimId, abjectId] of this.shimWidgetMap.entries()) {
      if (this.widgetToWindowShimId.get(abjectId) === windowId) {
        this.shimWidgetMap.delete(shimId);
        this.widgetIdToShimId.delete(abjectId);
        this.widgetToWindowShimId.delete(abjectId);
        this.spawnedWidgets.delete(abjectId);
      }
    }

    return true;
  }

  // ── Shim: addWidget ───────────────────────────────────────────────────

  private async addWidget(
    owner: AbjectId,
    config: {
      windowId: string;
      id: string;
      type: WidgetType;
      rect: { x: number; y: number; width: number; height: number };
      text?: string;
      style?: WidgetStyle;
      checked?: boolean;
      value?: number;
      options?: string[];
      selectedIndex?: number;
      placeholder?: string;
      monospace?: boolean;
      masked?: boolean;
      tabs?: string[];
      min?: number;
      max?: number;
      step?: number;
      url?: string;
      fit?: 'contain' | 'cover' | 'fill';
      alt?: string;
    }
  ): Promise<boolean> {
    const winAbjectId = this.shimWindowMap.get(config.windowId);
    if (!winAbjectId) return false;
    if (this.windowOwners.get(winAbjectId) !== owner) return false;

    require(this.uiServerId !== undefined, 'UIServer not set');

    // Create the appropriate widget Abject
    const baseConfig: WidgetConfig = {
      type: config.type,
      rect: config.rect,
      text: config.text,
      style: config.style,
      ownerId: winAbjectId,
      uiServerId: this.uiServerId!,
      theme: this.getThemeForWindow(winAbjectId),
    };

    let widget: WidgetAbject;

    switch (config.type) {
      case 'label':
        widget = new LabelWidget(baseConfig);
        break;
      case 'button':
        widget = new ButtonWidget(baseConfig);
        break;
      case 'textInput':
        widget = new TextInputWidget({
          ...baseConfig,
          placeholder: config.placeholder,
          masked: config.masked,
        } as TextInputWidgetConfig);
        break;
      case 'textArea':
        widget = new TextAreaWidget({
          ...baseConfig,
          monospace: config.monospace,
        } as TextAreaWidgetConfig);
        break;
      case 'checkbox':
        widget = new CheckboxWidget({
          ...baseConfig,
          checked: config.checked,
        } as CheckboxWidgetConfig);
        break;
      case 'progress':
        widget = new ProgressWidget({
          ...baseConfig,
          value: config.value,
        } as ProgressWidgetConfig);
        break;
      case 'divider':
        widget = new DividerWidget(baseConfig);
        break;
      case 'select':
        widget = new SelectWidget({
          ...baseConfig,
          options: config.options,
          selectedIndex: config.selectedIndex,
        } as SelectWidgetConfig);
        break;
      case 'tabBar':
        widget = new TabBarWidget({
          ...baseConfig,
          tabs: config.tabs,
          selectedIndex: config.selectedIndex,
        } as TabBarConfig);
        break;
      case 'slider':
        widget = new SliderWidget({
          ...baseConfig,
          min: config.min,
          max: config.max,
          step: config.step,
          value: config.value,
        } as SliderWidgetConfig);
        break;
      case 'image':
        widget = new ImageWidget({
          ...baseConfig,
          url: config.url,
          fit: config.fit,
          alt: config.alt,
        } as ImageWidgetConfig);
        break;
      default:
        return false;
    }

    await widget.init(this.bus, this.id);
    this.spawnedWidgets.add(widget.id);

    // Register WidgetManager as dependent of the widget (for event translation)
    await this.request(
      request(this.id, widget.id, 'addDependent', {})
    );

    // Track shim mappings
    this.shimWidgetMap.set(config.id, widget.id);
    this.widgetIdToShimId.set(widget.id, config.id);
    this.widgetToWindowShimId.set(widget.id, config.windowId);

    // Tell the WindowAbject to add this child
    await this.request(
      request(this.id, winAbjectId, 'addChild', {
        widgetId: widget.id,
        rect: config.rect,
      })
    );

    return true;
  }

  // ── Shim: updateWidget ────────────────────────────────────────────────

  private async updateWidget(
    widgetId: string,
    updates: Record<string, unknown>
  ): Promise<boolean> {
    const abjectId = this.shimWidgetMap.get(widgetId);
    if (!abjectId) return false;

    try {
      await this.request(
        request(this.id, abjectId, 'update', updates)
      );
      return true;
    } catch {
      return false;
    }
  }

  // ── Shim: removeWidget ────────────────────────────────────────────────

  private async removeWidget(widgetId: string): Promise<boolean> {
    const abjectId = this.shimWidgetMap.get(widgetId);
    if (!abjectId) return false;

    const windowShimId = this.widgetToWindowShimId.get(abjectId);
    if (windowShimId) {
      const winAbjectId = this.shimWindowMap.get(windowShimId);
      if (winAbjectId) {
        try {
          await this.request(
            request(this.id, winAbjectId, 'removeChild', { widgetId: abjectId })
          );
        } catch {
          // Window may be gone
        }
      }
    }

    // Destroy the widget (must use request() so the reply is consumed)
    try {
      await this.request(request(this.id, abjectId, 'destroy', {}));
    } catch {
      // Widget may be gone
    }

    this.shimWidgetMap.delete(widgetId);
    this.widgetIdToShimId.delete(abjectId);
    this.widgetToWindowShimId.delete(abjectId);
    this.spawnedWidgets.delete(abjectId);

    return true;
  }

  // ── Shim: getWidgetValue ──────────────────────────────────────────────

  private async getWidgetValue(widgetId: string): Promise<string> {
    const abjectId = this.shimWidgetMap.get(widgetId);
    if (!abjectId) return '';

    try {
      return await this.request<string>(
        request(this.id, abjectId, 'getValue', {})
      );
    } catch {
      return '';
    }
  }
