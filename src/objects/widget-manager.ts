/**
 * Widget Manager — factory that spawns WindowAbject and WidgetAbject instances.
 *
 * WidgetManager is a thin factory. It spawns widget Abjects on the bus and
 * returns their AbjectIds. All rendering, input handling, and state management
 * lives in the widget Abjects themselves.
 *
 * Primary API (returns AbjectIds for direct message passing):
 *   createWindowAbject, destroyWindowAbject, create (batch widget factory),
 *   createVBox, createHBox, createNestedVBox, createNestedHBox,
 *   createScrollableVBox, createNestedScrollableVBox, createCanvas
 *
 * Legacy shim (for ScriptableAbjects generated by ObjectCreator):
 *   createWindow, destroyWindow, addWidget, updateWidget,
 *   removeWidget, getWidgetValue, widgetEvent
 */

